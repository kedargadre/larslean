"""
prepare_data.py
Merges county-level and ED-level CSVs into two unified DataFrames,
saves them to ml/data/, and runs a full exploratory analysis,
writing the results to ml/data_exploration.md
"""

import pandas as pd
import numpy as np
import json
from pathlib import Path
from collections import Counter

# ── Paths ────────────────────────────────────────────────────────────────────
RAW = Path("data/real_data")
OUT  = Path("ml/data")
OUT.mkdir(parents=True, exist_ok=True)

# ── 1. Load raw files ─────────────────────────────────────────────────────────
cso    = pd.read_csv(RAW / "cso_employment.csv")
cso_ed = pd.read_csv(RAW / "cso_employment_ed.csv")
rtb    = pd.read_csv(RAW / "rtb_rent.csv")
rtb_ed = pd.read_csv(RAW / "rtb_rent_ed.csv")
seai   = pd.read_csv(RAW / "seai_ber.csv")
seai_ed= pd.read_csv(RAW / "seai_ber_ed.csv")
tii    = pd.read_csv(RAW / "tii_traffic.csv")
tii_ed = pd.read_csv(RAW / "tii_traffic_ed.csv")

# ── 2. Build county-level merged DataFrame ────────────────────────────────────
# Temporal files: aggregate to latest month (Dec 2024) per county
def latest_month(df):
    return df.sort_values("month").groupby("county").last().reset_index()

cso_latest  = latest_month(cso)
rtb_latest  = latest_month(rtb)
tii_latest  = latest_month(tii)

county_df = (
    cso_latest
    .merge(rtb_latest,  on="county", suffixes=("", "_rtb"))
    .merge(tii_latest,  on="county", suffixes=("", "_tii"))
    .merge(seai,        on="county")
)
# Drop duplicate month columns
county_df = county_df.loc[:, ~county_df.columns.duplicated()]
county_df.to_csv(OUT / "county_merged.csv", index=False)
print(f"county_merged.csv  shape={county_df.shape}")

# ── 3. Build ED-level merged DataFrame ───────────────────────────────────────
# Temporal ED files: aggregate to latest month per ed_id
def latest_month_ed(df):
    return df.sort_values("month").groupby("ed_id").last().reset_index()

cso_ed_l  = latest_month_ed(cso_ed)
rtb_ed_l  = latest_month_ed(rtb_ed)
tii_ed_l  = latest_month_ed(tii_ed)

ed_df = (
    cso_ed_l
    .merge(rtb_ed_l,   on=["ed_id", "ed_name", "county"], suffixes=("", "_rtb"))
    .merge(tii_ed_l,   on=["ed_id", "ed_name", "county"], suffixes=("", "_tii"))
    .merge(seai_ed,    on=["ed_id", "ed_name", "county"])
)
ed_df = ed_df.loc[:, ~ed_df.columns.duplicated()]

# Attach ed_type from GeoJSON
with open(RAW / "ireland_eds.geojson") as f:
    geo = json.load(f)
ed_type_map = {
    ft["properties"]["ed_id"]: ft["properties"]["ed_type"]
    for ft in geo["features"]
}
ed_df["ed_type"] = ed_df["ed_id"].map(ed_type_map)
ed_df.to_csv(OUT / "ed_merged.csv", index=False)
print(f"ed_merged.csv      shape={ed_df.shape}")

# ── 4. Analysis helpers ───────────────────────────────────────────────────────
def variance_flag(series):
    """Return 'ZERO VARIANCE ⚠️' if std == 0, else formatted std."""
    s = series.std()
    return f"ZERO VARIANCE ⚠️" if s == 0 else f"{s:.4f}"

def col_stats(df, col):
    s = df[col]
    return {
        "min":    round(s.min(), 4),
        "max":    round(s.max(), 4),
        "mean":   round(s.mean(), 4),
        "median": round(s.median(), 4),
        "std":    round(s.std(), 4),
        "nulls":  int(s.isnull().sum()),
        "unique": int(s.nunique()),
        "variance_flag": variance_flag(s),
    }

numeric_cols_county = [c for c in county_df.select_dtypes(include=np.number).columns]
numeric_cols_ed     = [c for c in ed_df.select_dtypes(include=np.number).columns]

